# 集合構造
basket_set = {"apple", "apple", "apple"}
print("集合構造 : ", basket_set)
print("集合構造 : ", type(basket_set))

# タプルの場合
basket_tuple = "apple", "apple", "apple"
print("タプル : ", basket_tuple)
print("タプル : ", type(basket_tuple))

# リストの場合
basket_list = ["apple", "apple", "apple"]
print("リスト : ", basket_list)
print("リスト : ", type(basket_list))

# 辞書構造の場合
basket_dict = {
    "apple":"りんご",
    "apple":"りんご",
    "apple":"りんご"
}
print("辞書構造 : ", basket_dict)
print("辞書構造 : ", type(basket_dict))